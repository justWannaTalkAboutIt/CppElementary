#include <iostream>
using namespace std;

int main5()
{
	//转义字符

	// 换行符 \n
	cout << "hello world\n";
	
	// 反斜杠 \\;
	cout << "\\"<< endl;

	//水平制表符 \t
	cout << "aa \t hello world" << endl; 
	cout << "aaaa \t hello world" << endl; 
	cout << "aaaaa \t hello world" << endl; 
	system("pause");
	return 0;
}